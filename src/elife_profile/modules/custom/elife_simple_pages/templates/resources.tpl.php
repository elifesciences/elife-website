<h3>Logo Color</h3>

<p>There are 10 colors used in the eLife logo. Based on the medium in which the logo will be presented, these colors will change. Print (CMYK): <img alt="" src="https://cdn.elifesciences.org/style-guide-images/elife-cmyk-chart.jpg" /> Web (RGB): <img alt="" src="https://cdn.elifesciences.org/style-guide-images/elife-rgb-chart.jpg" /> Web (RGB) hexidecimal values:</p>

<ul>
	<li>Brown: #6E3F19</li>
	<li>Dark Green: #046535</li>
	<li>Fuchsia: #CF0C4E</li>
	<li>Light Green: #629F43</li>
	<li>Light Blue: #0A9DD9</li>
	<li>Medium Blue: #0961AB</li>
	<li>Dark Blue: #273B81</li>
	<li>Purple: #732060</li>
	<li>Light Grey: #929497</li>
	<li>Medium Grey: #6D6E70</li>
</ul>

<p>If colors are not being used, the logo may be presented at 100% black, or 60% black. On a dark background, the logo may <strong>only</strong> appear in reverse white. PLEASE NOTE: On certain, extremely special occasions, eLife may allow individuals to bend these rules, but <strong>never</strong> break them, and always please ask first.</p>

